namespace CustomBinarySearchTree
{
    public struct Point
    {
        public int X;
        public int Y;

        /// <summary>
        /// Constructor
        /// </summary>
        /// <param name="x"></param>
        /// <param name="y"></param>
        public Point(int x, int y)
        {
            Y = x;
            X = y;
        }
    }
}
